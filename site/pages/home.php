<h2>Home</h2>
<p>This is a PHP framework for ease of use and adaptability. I am building it for personal use -  
but if anybody wishes to fork it please feel free! Any comments and ideas are welcome, you can email me on: contact at zephni.com</p>

<b>My ethos is to create a platform that provides a standard basis for website structure, but readily organised and quick to develop upon.
It should always use standard PHP and HTML without convoluting and over-complicating things.</b>

<p>Downloading the framework and running on a PHP webserver (Whether windows or unix) by default will display this help site for explaining how to use it.
As this is being written PHPZevelop is in very early stages and the help files are still being compiled.</p>

<p>The file structure for PHPZevelop is as follows: </p>

<pre class="code">
site/
phpzevelop/
.htaccess
config.php
index.php
</pre>

<p><span style="color: green;">site/</span>: For all the files regarding your website</p>

<p><span style="color: green;">phpzevelop/</span>: Contains dependant PHPZevelop files</p>

<p><span style="color: green;">.htaccess</span>: Is a set of rules for the working of the framework</p>

<p><span style="color: green;">config.php</span>: Main configuration</p>

<p><span style="color: green;">index.php</span>: Brings together the framework for usage</p>